<h4><b>Get things done with a 3-step flow: Browse &#8211; Buy &#8211; Done</b></h4>
<p><b>MicrojobEngine helps users quickly find reliable sellers to get things done with a 3-steps flow: Browse &#8211; Buy &#8211; Done</b></p>
<p>In order to do that, we developed a conversion-centered design for MicrojobEngine. In MicrojobEngine, a user has both roles as a buyer and a seller.</p>
<h4><b>1. Browse: Encouraging Interaction</b></h4>
<p><span style="font-weight: 400">The core value of a service marketplace relies on its ability to match the people looking for a service with the most suitable providers. This is the most important flow that MicrojobEngine focuses on.</span></p>
<p style="padding-left: 30px"><em><span style="font-weight: 400">Homepage: visitors can quickly determine whether your site provides what they need, then take a look at service categories and click on a category, a mJob or search with any keywords. </span><span style="font-weight: 400">(In MicrojobEngine, we call a service “mJob”, pronounced MicroJob)</span></em></p>
<p style="padding-left: 30px"><em><span style="font-weight: 400">In the mJob listing page, buyers can browse mJobs with no confusion since each mJob is displayed as a block with well-organized information. Buyers can be aware of which mJob is suitable for their need based on the image, description, ratings and status of every single mJob. Besides, buyer can also view the related mJobs using the advance filter (by category, tag or rating) without reloading the site.</span></em></p>
<p style="padding-left: 30px"><em><span style="font-weight: 400">MicrojobEngine offers an informative mJob detail page with clear service description, intuitive sample pictures, real order number, and reliable ratings/reviews. This helps the users to learn about a mJob better before deciding to order. Buyers can also view and select the extra tasks of a mJob so that buyers can make sure it matches their needs.</span></em></p>
<h4><b>2.</b><b> Smooth &amp; Clear Purchase &amp; Delivery Progress</b></h4>
<p>To encourage more purchases, buyers can place an order even before creating an account. We also understand how important communication is in the purchase process, to help both sides clearly comprehend the scope of work and stimulate the outcome results.</p>
<p style="padding-left: 30px"><i>Therefore, besides adding “order extra services” feature, MicrojobEngine offers a communication system which allows buyers and sellers to discuss directly and receive the notification whenever they have new messages. Thus, they can constantly communicate until the ordered mJob is finished.</i></p>
<p style="padding-left: 30px"><i>With a simple checkout interface, no login required and a highlighted call-to-action button, buyers will be more eager to complete his order. MicrojobEngine also supports users to change order information at check-out.</i></p>
<p style="padding-left: 30px"><i>Once an order has been placed, with a tracking order system, both buyers and seller can easily monitor the status of a mJob (Pending, Active, Late, Delivered, Finished, Disputing or Disputed). Each status relates closely to the Purchase &amp; Payment Flows which we will clarify more below.</i></p>
<h4><b>3. Get Things Done in The Best Way</b></h4>
<p>MicrojobEngine aims to bring satisfaction to both buyers and sellers and also keep them stay longer on your website because we understand successful user interactions and transactions will stimulate the growth of your website.</p>
<p style="padding-left: 30px"><em>Once a seller finishes his mJob and delivers to buyer, the buyer can choose to accept the work and change the status of mJob to Order Finished, then he can write a review for that seller. If he is not happy with the job, he may send a dispute request to admin.</em></p>
<p style="padding-left: 30px"><em>For Buyers, MicrojobEngine helps them monitor all mJobs easily. Sellers can change the status of a mJob (active, pause or archived) based on his schedule. When a mJob is paused, it won’t be available for orders, so he won’t be overwhelmed with the workload and can also keeps his reputation.</em></p>
